package com.example.implicitintent;

import java.util.Date;

public class News {

    public News(String name, String grade, String datePost, String price, String status, Integer hinh) {
        Name = name;
        Grade = grade;
        DatePost = datePost;
        Price = price;
        Status = status;
        Hinh = hinh;
    }

    public String Name;
    public String Grade;
    public String DatePost;
    public String Price;
    public String Status;
    public Integer Hinh;
}
